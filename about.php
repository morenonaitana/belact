		<h2>The story of Belact.</h2>
		<div class="content">
			<h3>The Belgian Group for Animal Cell Technology (BELACT) was created in 1992
			by P. Dewaele, L. Fabry, D. Malarme, A. Miller and J. Werenne. Since its foundation,
			BELACT has a strong affiliated history with the pan-European sister society, the
			European Society for Animal Cell Technology <a href="www.esact.org" target="_blank">(ESACT)</a>.</h3>
			<p class="p1">Belgium is one of the largest plants of bio/pharma industries and is hosting leading research institutes working in
			biochemical engineering applied to mammalian cells. BELACT seeks at bringing
			together scientists and engineers, from Belgium and countries of close boarders
			(France, Luxembourg, The Netherlands etc.), working in the fields of animal cell
			culture.</p>
			<p class="p1">Through the organization of regular topical events, BELACT aims at
			stimulating close scientific collaborations between academia and industry. The group
			is interested by the whole chain of bioproduction with animal cells: from cell
			lines/population characterization, bioprocess development, design of bioreactors,
			biomaterials, tissue engineering, recombinant protein production etc.</p>
